function fallbackDescription(title, family) {
  return family.fallback.replace(/^Provides (?:an? |the )?/, `Provides the ${title} `);
}

function capabilityBullets(source, family) {
  const bullets = [...family.bullets];
  const checks = [
    ['connect', /\basync\s+connect\b|\bconnect\s*\(/, 'Includes a connection flow for account or credential setup.'],
    ['setup', /\bsetup\s*:/, 'Includes setup guidance for required credentials or provider configuration.'],
    ['build', /\basync\s+build\b|\bbuild\s*\(/, 'Implements build behavior used by sh1pt target workflows.'],
    ['publish', /\basync\s+publish\b|\bpublish\s*\(/, 'Implements publish behavior used by sh1pt promotion or release workflows.'],
    ['deploy', /\basync\s+deploy\b|\bdeploy\s*\(/, 'Implements deploy behavior for runtime or infrastructure workflows.'],
    ['stats', /\basync\s+stats\b|\bstats\s*\(/, 'Exposes stats or reporting data for sh1pt dashboards and automation.'],
    ['tracking', /\bgetTrackingLink\s*\(/, 'Can generate tracking links for campaigns or partner programs.'],
    ['send', /\bsendMessage\s*\(|\bpostMessage\s*\(|\bsend\s*\(/, 'Can send messages or posts through the connected provider.'],
  ];
  for (const [, re, bullet] of checks) {
    if (re.test(source) && !bullets.includes(bullet)) bullets.push(bullet);
  }
  return bullets.slice(0, 5).map((bullet) => `- ${ascii(bullet)}`);
}

function titleFromPackageName(packageName, rel) {
  const name = packageName ? packageName.split('/').pop() ?? packageName : basename(rel);
  return name
    .replace(/^sh1pt-/, '')
    .replace(/^(package|service|target|adapter)-/, '')
    .split('-')
    .filter(Boolean)
    .map((part) => part.length <= 3 ? part.toUpperCase() : part[0].toUpperCase() + part.slice(1))
    .join(' ');
}

function sentence(text) {
  const trimmed = String(text).trim();
  if (!trimmed) return 'sh1pt module.';
  return /[.!?]$/.test(trimmed) ? trimmed : `${trimmed}.`;
}

function ascii(text) {
  return String(text)
    .replace(/[–—]/g, '-')
    .replace(/[‘’]/g, "'")
    .replace(/[“”]/g, '"')
    .replace(/…/g, '...')
    .replace(/→/g, '->')
    .replace(/·/g, '-')
    .replace(/[^\x09\x0A\x0D\x20-\x7E]/g, '');
}

async function exists(path) {
  try {
    await stat(path);
    return true;
  } catch {
    return false;
  }
}

async function readdirSafe(dir) {
  try {
    return await readdir(dir);
  } catch {
    await mkdir(dir, { recursive: true });
    return [];
  }
}
