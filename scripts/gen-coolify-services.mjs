function toSlug(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

function pkgJson(slug) {
  return {
    name: `@profullstack/sh1pt-service-${slug}`,
    version: '0.1.14',
    type: 'module',
    main: './src/index.ts',
    scripts: {
      build: 'tsc -p tsconfig.json',
      typecheck: 'tsc -p tsconfig.json --noEmit',
      prepublishOnly: 'pnpm build',
    },
    dependencies: {
      '@profullstack/sh1pt-core': 'workspace:*',
    },
    license: 'MIT',
    repository: {
      type: 'git',
      url: 'git+https://github.com/profullstack/sh1pt.git',
      directory: `services/${slug}`,
    },
    homepage: 'https://sh1pt.com',
    bugs: 'https://github.com/profullstack/sh1pt/issues',
    files: ['dist'],
    publishConfig: {
      access: 'public',
      main: './dist/index.js',
      types: './dist/index.d.ts',
      exports: {
        '.': {
          types: './dist/index.d.ts',
          import: './dist/index.js',
        },
      },
    },
  };
}

const tsconfig = {
  extends: '../../tsconfig.base.json',
  compilerOptions: { outDir: 'dist', rootDir: 'src' },
  include: ['src/**/*'],
};

function indexTs(slug, name, category, description) {
  return `import { defineService } from '@profullstack/sh1pt-core';

export default defineService({
  id: 'service-${slug}',
  label: ${JSON.stringify(name)},
  category: ${JSON.stringify(category)},
  description: ${JSON.stringify(description)},
  coolifyTemplate: ${JSON.stringify(slug)},
  homepageUrl: 'https://coolify.io/services',
});
`;
}

const testTs = `import { smokeTest } from '@profullstack/sh1pt-core/testing';
import adapter from './index.js';

smokeTest(adapter, { idPrefix: 'service' });
`;

mkdirSync(SERVICES_DIR, { recursive: true });

const seen = new Set();
let written = 0;
for (const [name, category, description] of services) {
  const slug = toSlug(name);
  if (seen.has(slug)) {
    console.error(`duplicate slug skipped: ${slug} (${name})`);
    continue;
  }
  seen.add(slug);
  const dir = join(SERVICES_DIR, slug);
  mkdirSync(join(dir, 'src'), { recursive: true });
  writeFileSync(join(dir, 'package.json'), JSON.stringify(pkgJson(slug), null, 2) + '\n');
  writeFileSync(join(dir, 'tsconfig.json'), JSON.stringify(tsconfig, null, 2) + '\n');
  writeFileSync(join(dir, 'src', 'index.ts'), indexTs(slug, name, category, description));
  writeFileSync(join(dir, 'src', 'index.test.ts'), testTs);
  written++;
}

console.log(`Generated ${written} services in ${SERVICES_DIR}`);
